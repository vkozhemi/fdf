#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int j;

	j = 0;
	if (f && s)
	{
		while (s[j] != '\0')
		{
			f(j, &s[j]);
			j++;
		}
	}
}
